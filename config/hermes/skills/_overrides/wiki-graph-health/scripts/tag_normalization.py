def extract_tags_from_frontmatter(front):
    """Extract tag strings from frontmatter text."""
    tags = []
    # Block format: tags:\n  - tag1\n  - tag2 (double-space indent)
    # Also handle: tags:\n - tag1\n - tag2 (single-space indent)
    block_match = re.search(r'^tags:\n((?:[ \t]*- .*\n?)+)', front, re.MULTILINE)
    if block_match:
        for line in block_match.group(1).split('\n'):
            line = line.strip()
            if line.startswith('- '):
                tag = line[2:].strip().strip('"\'')
                if tag:
                    tags.append(tag)
        return tags
    # Inline format: tags: [tag1, tag2]
    inline_match = re.search(r'^tags:\s*\[(.+)\]', front, re.MULTILINE)
    if inline_match:
        for t in inline_match.group(1).split(','):
            tag = t.strip().strip('"\'').strip()
            if tag:
                tags.append(tag)
        return tags
    # Simple: tags: tagname
    simple_match = re.search(r'^tags:\s*(\S+)', front, re.MULTILINE)
    if simple_match:
        tags.append(simple_match.group(1).strip().strip('"\''))
    return tags

def normalize_page(content):
    """Normalize tags in frontmatter. Returns modified content or None."""
    m = re.match(r'^(---\n)(.*?)(\n---)', content, re.DOTALL)
    if not m:
        return None
    opener, front, closer = m.group(1), m.group(2), m.group(3)
    body = content[m.end():]  # CRITICAL: preserve everything after frontmatter!
    if not re.search(r'^tags:', front, re.MULTILINE):
        return None
    tags = extract_tags_from_frontmatter(front)
    if not tags:
        return None
    changed = False
    normalized = []
    for tag in tags:
        new_tag = TAG_NORMALIZATION.get(tag, tag)
        if new_tag != tag:
            changed = True
        if new_tag:
            normalized.append(new_tag)
    seen = set()
    deduped = []
    for tag in normalized:
        key = tag.lower()
        if key not in seen:
            seen.add(key)
            deduped.append(tag)
        elif tag != key:
            changed = True
    if not changed:
        return None
    new_tags_block = 'tags:\n'
    for tag in deduped:
        new_tags_block += f'  - {tag}\n'
    new_front = re.sub(
        r'^tags:.*(?:\n|$)(?:[ \t]+- .*\n?)*',
        new_tags_block,
        front,
        count=1,
        flags=re.MULTILINE
    )
    # Guard against phantom modifications: only return if content actually changed
    new_content = opener + new_front + closer + body
    if new_content == content:
        return None
    # Return FULL content: frontmatter + body
    return new_content

def main():
    dry_run = '--dry-run' in sys.argv
    modified = 0
    scanned = 0
    for root_dir in ['entities', 'concepts', 'comparisons']:
        for root, dirs, files in os.walk(os.path.join(wiki, root_dir)):
            for f in files:
                if not f.endswith('.md'):
                    continue
                path = os.path.join(root, f)
                scanned += 1
                with open(path) as fh:
                    content = fh.read()
                new_content = normalize_page(content)
                if new_content:
                    if not dry_run:
                        with open(path, 'w') as fh:
                            fh.write(new_content)
                    rel = os.path.relpath(path, wiki)
                    status = "[DRY RUN]" if dry_run else "[MODIFIED]"
                    print(f"  {status} {rel}")
                    modified += 1
    print(f"\nScanned: {scanned} pages")
    print(f"{'Would modify' if dry_run else 'Modified'}: {modified} pages")

if __name__ == '__main__':
    main()
